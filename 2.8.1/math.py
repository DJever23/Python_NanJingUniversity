import math
print(math.ceil(3.6))  #ceil天花板 向上取整
print(math.pi)         #π
print(math.e)          #自然数e
print(math.floor(3.2)) #floor地板 向下取整
print(math.pow(2,3))   #power 次方
print(math.sqrt(4))    #sqrt 开方
print(math.degrees(3.14)) #角度转成弧度
print(math.radians(180))  #弧度转成角度